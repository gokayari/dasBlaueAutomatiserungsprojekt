package org;

public class Rechner {

    public double addition(double a, double b){
        return a+b;
    }

    public double subtraktion(double a, double b){
        return a-b;
    }

    public double multiplikation(double a, double b){
        return a*b;
    }

    public double division(double a, double b){
        return a/b;
    }
}
